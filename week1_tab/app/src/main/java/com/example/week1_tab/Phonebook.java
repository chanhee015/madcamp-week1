package com.example.week1_tab;

public class Phonebook {
    public String name;
    public String number;

    //getter
    public String getName() {
        return this.name;
    }
    public String getNumber() {
        return this.number;
    }

    //setter
    public void setName(String name) {
        this.name = name;
    }
    public void setNumber(String number) {
        this.number = number;
    }

}
